<h2 id="后记" tabindex="-1"><a class="header-anchor" href="#后记"><span>后记</span></a></h2>
<p>阅读csdiy的时候，作者后记的一段话给我印象很深</p>
<blockquote>
<p>但我觉得，本科教育至少得展现它应有的诚意，一种分享知识的诚意，一种以人为本的诚意，一种注重学生体验的诚意。</p>
</blockquote>
<p>笔者虽然在入学之前就听说了大学老师上课和高中完全不一样，但是上的第一节专业课还是刷新了自己的认知：没有编译器，没有IDE,没有优雅的代码；有的只是一本讲上个世纪c语言的教材和小红书搜到的陈年ppt,老师在讲台上念着ppt，ppt在重复教材的话。</p>
<p>好像大家都默认了什么，大学大学，大了要自己学，上课就是为了不被记旷课，有毅力者下课后自学，没毅力者就活该保研轮不上，考研考不上，工作找不到，毕业被市场淘汰。</p>
<p>但是，上课学不到东西，或者学的效率质量远远不如找网上的资料自学，上课就是为了上课，这本身不荒唐么？</p>
<p>笔者在想分析这个问题，从两个方面来谈</p>
